# 數字運算
x=33+5
x=3/5 # 小數除法，結果為小數點
x=7//3 # 整數除法，除到結果為整數
x=2**5 # 次方** 2的5次方
x=4**0.5 # 開根號即為4**0.5
x=5%2 # 取餘數，所以為1

x=2+3
